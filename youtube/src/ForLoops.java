public class ForLoops {

    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) {
            System.out.println("I love dogs!");
        }
        for (int i = 0; i < 15; i++) {
            System.out.println(i);
        }
        //print out all values in an arrray

        int[] grades = {98, 100, 101, 83};

        for (int i = 0; i < grades.length; i++) {
            System.out.println(i);
        }
    }

}
