public class Arrays {
    //swapping flowers
    public static void main(String[] args) {
        System.out.println("Welcome to Zombieland!");

        String backpack[] = {"Roses", "Daffodils", "Clementines"};
        String zombies[] = {"Garden", "Meadow", "Balcony"};
        int hatedZombiesNums[] = {4, 3, 2, 1};

        System.out.println("Backpack items are");
        System.out.println(backpack[0]);
        System.out.println(backpack[1]);
        System.out.println(backpack[2]);

        System.out.println("These are all the Zombie locations:");
        System.out.println(zombies[0]);
        System.out.println(zombies[1]);
        System.out.println(zombies[2]);

        System.out.println(hatedZombiesNums[3]);

    }
}
